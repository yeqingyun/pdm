				str += '"mailGroup.groupLevel":"'+$("#groupLevel").val()+'",';
				str += '"mailGroup.status":"'+$("#status").val()+'",';
				str += '"mailGroup.createDate":"'+$("#createDate").val()+'",';
				str += '"mailGroup.lastUpd":"'+$("#lastUpd").val()+'",';
				str += '"mailGroup.lastUpdDate":"'+$("#lastUpdDate").val()+'"';
				str += '}';

