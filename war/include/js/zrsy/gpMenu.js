function sea() {
	gridManager.setOptions({
		parms: [
			{name: 'gpMenu.gpId', value: $("#gpId").val()},
			{name: 'gpMenu.menuId', value: $("#menuId").val()}

		]
	});
	gridManager.loadData();
}
function sav() {
	$.ligerDialog.confirm('确定要保存记录？', 
		function (type) { 
			if(type) {
				var str = '{';
				str += '"gpMenu.gpId":"'+$("#gpId").val()+'",';
				str += '"gpMenu.menuId":"'+$("#menuId").val()+'"';
				str += '}';

				$.post("GpMenu!sav.shtml",
					JSON.parse(str),
					function(data) {
						$.ligerDialog.success(data);
					},
					"text"
				);
			}
		}
	);
}
