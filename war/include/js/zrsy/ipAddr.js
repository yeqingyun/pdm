function saver() {
	$.ligerDialog.confirm('确定要保存记录？', 
		function (type) { 
			if(type) {
				var str = '{';
				str += '"ipAddr.ipId":"'+$("#ipId").val()+'",';
				str += '"ipAddr.isWide":"'+$("#isWide").val()+'",';
				str += '"ipAddr.status":"'+$("#status").val()+'",';
				str += '"ipAddr.ipSegment":"'+$("#ipSegment").val()+'",';
				str += '"ipAddr.addrDesc":"'+$("#addrDesc").val()+'"';
				str += '}';
				alert(str);
				$.post("IpAddr!sav.shtml",
					JSON.parse(str),
					function(data) {
						$.ligerDialog.success(data);
					},
					"text"
				);
			}
		}
	);
}
