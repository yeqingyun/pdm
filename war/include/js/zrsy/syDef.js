function f_onCheckAllRow(checked) {
	for (var rowid in this.records) {
		if(checked)
			addCheckedCustomer(this.records[rowid]['syId']);
		else
			removeCheckedCustomer(this.records[rowid]['syId']);
	}
}
var checkedCustomer = [];
function findCheckedCustomer(syId) {
	for(var i =0;i<checkedCustomer.length;i++) {
		if(checkedCustomer[i] == syId) return i;
	}
	return -1;
}
function addCheckedCustomer(syId) {
	if(findCheckedCustomer(syId) == -1)
		checkedCustomer.push(syId);
}
function removeCheckedCustomer(syId){
	var i = findCheckedCustomer(syId);
	if(i==-1) return;
	checkedCustomer.splice(i,1);
}
function f_isChecked(rowdata) {
	if (findCheckedCustomer(rowdata.syId) == -1)
		return false;
	return true;
}
function f_onCheckRow(checked, data){
	if (checked)
		addCheckedCustomer(data.syId);
	else
		removeCheckedCustomer(data.syId);
}
function f_onDblClickRow(data){
	vie(data);
}
