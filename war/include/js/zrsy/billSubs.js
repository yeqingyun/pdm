function saver() {
	$.ligerDialog.confirm('确定要保存记录？', 
		function (type) { 
			if(type) {
				var str = '{';
				str += '"billSubs.id":"'+$("#id").val()+'",';
				str += '"billSubs.syId":"'+$("#syId").val()+'",';
				str += '"billSubs.subs":"'+$("#subs").val()+'",';
				str += '"billSubs.currSn":"'+$("#currSn").val()+'",';
				str += '"billSubs.state":"'+$("#state").val()+'"';
				str += '}';

				$.post("BillSubs!sav.shtml",
					JSON.parse(str),
					function(data) {
						$.ligerDialog.success(data);
					},
					"text"
				);
			}
		}
	);
}
