function saver() {
	$.ligerDialog.confirm('确定要保存记录？', 
		function (type) { 
			if(type) {
				var str = '{';
				str += '"wfRdSign.createDate":"'+$("#createDate").val()+'",';
				str += '"wfRdSign.taskId":"'+$("#taskId").val()+'",';
				str += '"wfRdSign.userId":"'+$("#userId").val()+'",';
				str += '"wfRdSign.status":"'+$("#status").val()+'",';
				str += '"wfRdSign.signText":"'+$("#signText").val()+'",';
				str += '"wfRdSign.wfNoId":"'+$("#wfNoId").val()+'"';
				str += '}';

				$.post("WfRdSign!sav.shtml",
					JSON.parse(str),
					function(data) {
						$.ligerDialog.success(data);
					},
					"text"
				);
			}
		}
	);
}
